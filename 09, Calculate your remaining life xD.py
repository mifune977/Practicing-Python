# your life calculation if you live 90 years.

age = input("What's your current age?\n")

remaining_days = (90 - int(age)) * 365
remaining_weeks = (90 - int(age)) * 52
remaining_years = (90 - int(age)) * 1

print(f"if you live 90 years then you have left, \n{remaining_days} days, \n{remaining_weeks} weeks \n& {remaining_years} years my friend.\n\nBest enjoy now, \nBecause in the long run, WE ALL DEAD!")

